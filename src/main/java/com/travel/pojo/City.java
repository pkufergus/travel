package com.travel.pojo;

public class City {
	private String cityCode;
	
	private String englishName;
	
	private String chineseName;
	
	private String traditionalChineseName;
	
	private String undone;
	
	private String country;
	
	private Integer city_level;
	
	
	
	

	public Integer getCity_level() {
		return city_level;
	}

	public void setCity_level(Integer city_level) {
		this.city_level = city_level;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getCityCode() {
		return cityCode;
	}

	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}


	public String getEnglishName() {
		return englishName;
	}

	public void setEnglishName(String englishName) {
		this.englishName = englishName;
	}

	public String getChineseName() {
		return chineseName;
	}

	public void setChineseName(String chineseName) {
		this.chineseName = chineseName;
	}

	public String getTraditionalChineseName() {
		return traditionalChineseName;
	}

	public void setTraditionalChineseName(String traditionalChineseName) {
		this.traditionalChineseName = traditionalChineseName;
	}

	public String getUndone() {
		return undone;
	}

	public void setUndone(String undone) {
		this.undone = undone;
	}
	
	
}
